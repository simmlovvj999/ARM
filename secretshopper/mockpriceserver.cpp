QJsonDocument MockPriceServer::getPrices(const QString& chain,
                                          const QStringList& productNames)
{
    if (s_db.isEmpty()) {
        s_db = buildDatabase();
    }


    const ChainPrices& cp = s_db[chain];
    QJsonArray products;

    for (const QString& name : productNames) {
        bool found = false;

        // Ищем точное совпадение ИЛИ по подстроке (без учёта регистра)
        for (auto it = cp.begin(); it != cp.end(); ++it) {
            if (it.key().contains(name, Qt::CaseInsensitive)) {
                const PriceEntry& e = it.value();
                products.append(QJsonObject{
                    {"name", it.key()},
                    {"price", e.price},
                    {"price_card", e.priceCard},
                    {"unit", e.unit},
                    {"in_stock", true}
                });
                found = true;
                break;
            }
        }
    }

    return QJsonDocument(QJsonObject{
        {"store",    chain},
        {"products", products}
    });
}

